    public void calcularSubtotal() {
        subTotal = 0.00;
        subTotal = Double.parseDouble(txtSub1.getText()) + Double.parseDouble(txtSub2.getText()) +
                Double.parseDouble(txtSub3.getText())+ Double.parseDouble(txtSub4.getText());
        txtSubtotal12.setText(String.valueOf(subTotal));
    }
    
    public void calcularIva() {
        iva = 0.12 * subTotal;
        txtIva.setText(String.valueOf(iva));
    }
    /**
     * Metodo que calcula el Total de la Factura
     */
    public void calcularTotal() {
        calcularSubtotal();
        calcularIva();
        txtTOTAL.setText(String.valueOf((subTotal) + (iva)));
    }
    
