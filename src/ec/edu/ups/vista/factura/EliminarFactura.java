    private void btnBuscarFacturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarFacturaActionPerformed
        int codigo = Integer.parseInt(txtCodigo.getText());
        factura = controlFactura.buscar(codigo);
        cliente = controlCliente.read(codigo);

        txtNomCliente.setText(cliente.getNombre());
        txtDireCliente.setText(cliente.getDireccion());
        txtTeleCiente.setText(cliente.getTelefono());
    }//GEN-LAST:event_btnBuscarFacturaActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        factura.setCodigo(factura.getCodigo());
        controlFactura.eliminar(factura);
        
        JOptionPane.showMessageDialog(this, "Factura Eliminada correctamente", "Ventana Eliminar Factura", JOptionPane.OK_OPTION);
        
        txtNomCliente.setText("");
        txtDireCliente.setText("");
        txtTeleCiente.setText("");
    }//GEN-LAST:event_btnEliminarActionPerformed


